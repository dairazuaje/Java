/*----------------------------------------------------------------------------------------------------------------------
 * Enumerator used to restrict what wheel sizes users can choose from
 * -------------------------------------------------------------------------------------------------------------------*/

public enum WheelSize
{
  TEN(10), 
  TWELVE(12), 
  FOURTEEN(14), 
  SIXTEEN(16), 
  EIGHTEEN(18), 
  TWENTY(20), 
  TWENTY4(24), 
  TWENTYNINE(29)
  ;
  
  private final int value; 
  
 /**********************************************************************************************************************
   * WheelSize enum method
   * -------------------------------------------------------------------------------------------------------------------
   * Function:
   *   Method used to set private variable value equal to enum number field 
   * ******************************************************************************************************************/

  
  WheelSize(int value)
  { //Begin method
    this.value = value;
  } //End method
 
 /**********************************************************************************************************************
   * getValue method
   * -------------------------------------------------------------------------------------------------------------------
   * Function:
   *   Method used to get and return value of enum number field 
   * ******************************************************************************************************************/  
  
  public int getValue()
  { //Begin getValue
    return value;
  } //End getValue
}